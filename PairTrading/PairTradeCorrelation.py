#The premise of this strategy is as follows: find two correlated assets
#e.g Coke & Pepsi, Visa & Mastercard, Ford & GM, and when the price relationship diverges
#e.g one goes up more than the other, assume they will revert to their historical spread
#Go long on the under performer and short the over performer
#Profit when the spread reverts back to norm
# you profit from a mean reversion, not from the absolute price moves
# this works well in a sideways market when stocks, within a certain market, tend to fluctuate up and down
# in trending markets - this strategy doesn't work great
# let's say that NVIDIA skyrockets due to AI hype while AMD lags
# if NVIDIA keeps ripping upward, you lose on the short, and the spread widens

